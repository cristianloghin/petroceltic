			<div>
				<p>During 2014, Petroceltic made significant progress towards the development of the strategically important Ain Tsila gas condensate field, which lies within the Isarene concession in the Illizi basin in south east Algeria.</p>
				<p>The Front End Engineering and Design (‘FEED’) contract was awarded in September and the Gas Sales Agreement was signed and approved by the Algerian authorities in November.</p>
			</div>
			<div>
				<p>Of particular importance during the year was the successful completion of a second farm-out of a 18.375% interest in the Ain Tsila field to Sonatrach, the National oil and gas company of Algeria.</p>
				<p>2015 will see the completion of the FEED studies and the tender and award of the EPC contract for the major project construction contracts.</p>
