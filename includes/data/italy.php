				<p>The “Restart Italy” decree, passed into law in November 2014, builds on the National Energy Strategy of 2013 in reaffirming the strategic nature of domestic hydrocarbon exploration and production activities and contains a number of measures aimed at facilitating progress in the sector whilst aligning regulation with offshore safety EU directive. These include the introduction of the “Single Permit” which aims at simplifying permitting by combining exploration and production phases into a single licence and transferring the responsibility for onshore environmental permits from the Regions to the Ministry of the Environment.</p>
